package week2.exUpTo36.ex36Full;

public class ex28FromHundredToOne {
/*
Create a program that prints the integers (whole numbers) from 100 to 1.
Tip: Assign the variable you use in the condition of the loop
a initial value of 100 and then subtract one on each iteration of the loop.
 */

    public static void main(String[] args) {
        int i = 100;
        while(i>0){
            System.out.println(i);
            i--;
        }
    }
}
